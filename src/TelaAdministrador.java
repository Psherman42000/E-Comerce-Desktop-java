    private void adicionarprodutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarprodutoActionPerformed
        if(nome.getText().equals("")||descricao.getText().equals("")||precovenda.getText().equals("")||precocusto.getText().equals("")||con.getArquivo()==null){
            JOptionPane.showMessageDialog(null,"√© preciso selecionar uma outra foto para o produto e nao podem haver campos em branco");
        }
        else{
            try{
                con.inserirProduto(nome.getText(), descricao.getText(), Float.parseFloat(precocusto.getText()), Float.parseFloat(precovenda.getText()));
                
                produ=con.getProdutos();
                if(produ!=null){
                    nome.setText(produ.get(0).getNome());
                    descricao.setText(produ.get(0).getDescricao());
                    precocusto.setText(Float.toString(produ.get(0).getPrecocusto()));
                    precovenda.setText(Float.toString(produ.get(0).getPrecovenda()));
                    foto.setIcon(produ.get(0).getFoto());
                }
                JOptionPane.showMessageDialog(null,"produto cadastrado com sucesso");
                setTam(0);
                nprod.setText("PRODUTO: "+(getTam()+1));
            }
            catch(Exception e){
                        JOptionPane.showMessageDialog(null,"os valores do custo ou do preco de venda nao estao de acordo com o pedido");
            }
        }
                    
    }//GEN-LAST:event_adicionarprodutoActionPerformed

