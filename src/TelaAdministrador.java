    private void removerprodutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerprodutoActionPerformed
        if(produ.size()>0){
            int id=con.getIdProduto(nome.getText(), descricao.getText(), Float.parseFloat(precovenda.getText()));
            if(con.naoExistemPedidos(con.getIdProduto(nome.getText(), descricao.getText(), Float.parseFloat(precovenda.getText())))){
                con.removeCarrinho(con.getIdProduto(nome.getText(), descricao.getText(), Float.parseFloat(precovenda.getText())));
                con.removerProduto(nome.getText(),descricao.getText(),Float.parseFloat(precocusto.getText()),Float.parseFloat(precovenda.getText()));
                
                try {
                    produ=con.getProdutos();
                } catch (IOException ex) {
                    Logger.getLogger(TelaAdministrador.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(TelaAdministrador.class.getName()).log(Level.SEVERE, null, ex);
                }
                       if(produ!=null){
                           nome.setText(produ.get(0).getNome());
                           descricao.setText(produ.get(0).getDescricao());
                           precocusto.setText(Float.toString(produ.get(0).getPrecocusto()));
                           precovenda.setText(Float.toString(produ.get(0).getPrecovenda()));
                           foto.setIcon(produ.get(0).getFoto());

                       }
                       else{
                           nome.setText("");
                           descricao.setText("");
                           precocusto.setText("");
                           precovenda.setText("");
                           foto.setIcon(null);
                       }
                       JOptionPane.showMessageDialog(null, "produto removido com sucesso");
                       setTam(0);
                       nprod.setText("PRODUTO: "+(getTam()+1));
            }else{
                JOptionPane.showMessageDialog(null,"EXISTEM PEDIDOS RELACIONADOS A ESTE PRODUTO, IMPOSSIVEL REMOVER");
            }
        }else{
            JOptionPane.showMessageDialog(null,"nao existem mais produtos para remover");
        }
            
    }//GEN-LAST:event_removerprodutoActionPerformed

